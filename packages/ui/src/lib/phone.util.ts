import parsePhoneNumber, {
  isValidPhoneNumber as isValidPhoneNumberDefaultImpl,
} from "libphonenumber-js/min";

function isValidPhoneNumber(
  value?: string,
  isRequired: boolean = true,
): boolean {
  if (!value && !isRequired) {
    return true;
  } else if (!value) {
    return false;
  }

  try {
    const parsed = parsePhoneNumber(value);
    const validator = CALLING_CODE_TO_LENGTH.find(
      (item) => item.dialCode === parsed?.countryCallingCode,
    );

    if (validator && parsed) {
      return (
        parsed.nationalNumber.length <= validator.maxLength &&
        parsed.nationalNumber.length >= validator.minLength
      );
    }

    return false;
  } catch (e) {
    console.warn(e);
    return false;
  }
}

export { isValidPhoneNumber, isValidPhoneNumberDefaultImpl };
